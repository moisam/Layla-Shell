#include <stdlib.h>
#include "../cmd.h"
#include "../debug.h"


int logout(int argc, char **argv)
{
    /*
     * NOTE: we perform proper logout in the exit_gracefully() function
     *       of exit.c.
     */
    __exit(argc, argv);
    __builtin_unreachable();
}
